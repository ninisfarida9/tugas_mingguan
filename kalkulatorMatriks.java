    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        int matriksA[][] = new int [2][2];
        matriksA[0][0] = Integer.parseInt(A111.getText());
        matriksA[0][1] = Integer.parseInt(A122.getText());
        matriksA[1][0] = Integer.parseInt(A211.getText());
        matriksA[1][1] = Integer.parseInt(A222.getText());
        
        int matriksB[][] = new int [2][2];
        matriksB[0][0] = Integer.parseInt(B111.getText());
        matriksB[0][1] = Integer.parseInt(B122.getText());
        matriksB[1][0] = Integer.parseInt(B211.getText());
        matriksB[1][1] = Integer.parseInt(B222.getText());
        
        int matriksC[][] = new int [2][2];
        
        if (jumlah.isSelected()){
           for(int i = 0; i<matriksA.length;i++) {  
                for(int j = 0; j<matriksA[0].length;j++) {
                    matriksC [i][j] = matriksA [i][j] + matriksB [i][j];
                    
                }
            }
            C111.setText(String.valueOf(matriksC[0][0]));
            C122.setText(String.valueOf(matriksC[0][1]));
            C211.setText(String.valueOf(matriksC[1][0]));
            C222.setText(String.valueOf(matriksC[1][1]));
        }else if (kurang.isSelected()){
         for(int i = 0; i<matriksA.length;i++) {  
                for(int j = 0; j<matriksA[0].length;j++) {
                    matriksC [i][j] = matriksA [i][j] - matriksB [i][j];
                    
                }
            }
            C111.setText(String.valueOf(matriksC[0][0]));
            C122.setText(String.valueOf(matriksC[0][1]));
            C211.setText(String.valueOf(matriksC[1][0]));
            C222.setText(String.valueOf(matriksC[1][1]));   
        }else if(kali.isSelected()){
            matriksC[0][0] = (matriksA[0][0]*matriksB[0][0]) + (matriksA [0][1]*matriksB[1][0]);
            matriksC[0][1] = (matriksA[0][0]*matriksB[0][1]) + (matriksA [0][1]*matriksB[1][1]);
            matriksC[1][0] = (matriksA[1][0]*matriksB[0][0]) + (matriksA [1][1]*matriksB[1][0]);
            matriksC[1][1] = (matriksA[1][0]*matriksB[0][1]) + (matriksA [1][1]*matriksB[1][1]);
        
            C111.setText(String.valueOf(matriksC[0][0]));
            C122.setText(String.valueOf(matriksC[0][1]));
            C211.setText(String.valueOf(matriksC[1][0]));
            C222.setText(String.valueOf(matriksC[1][1]));
            
        }  
        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

